 /*(Adaptado de ASCENCIO e CAMPOS, 2008) Faça um programa que receba o salário de 
um funcionário, calcule e mostre o novo salário, sabendo-se que este sofreu um 
aumento de 25%.*/ 

#include <stdio.h>
#include <stdlib.h>

int main(int argc, char *argv[]) {
	
	int salario, novosalario;
	
	printf("digite seu salario:\n");
	scanf("%d", &salario);
	
	novosalario = salario + salario*(1.0/4.0); 
	
	printf("seu novo salario sera:%d\n", novosalario);
	return 0;
}
